package martin.matobuat03;

public class martint03e01 {
    
    public static void main(String[] args) {
        
        // Constante que almacena cuántas veces se quiere imprimir la frase:
        final int NUM_VECES = 10;
        
        // Se muestra la frase 'NUM_VECES' veces:
        for (int i=0; i<NUM_VECES; i++){
            System.out.println("Esto es un bucle");
        }
           
    }
    
}
